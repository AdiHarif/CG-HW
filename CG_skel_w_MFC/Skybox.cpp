const string Skybox::faces_paths[] = {
    "skybox/right.jpg",
    "skybox/left.jpg",
    "skybox/top.jpg",
    "skybox/bottom.jpg",
    "skybox/front.jpg",
    "skybox/back.jpg"
};

Skybox::Skybox() {
    faces_count = SKYBOX_FACES_COUNT;
    loadCubemap();

    glGenVertexArrays(1, &vao);
    glBindVertexArray(vao);

    glGenBuffers(1, &vbo);
    glBindBuffer(GL_ARRAY_BUFFER, vbo);
    glBufferData(GL_ARRAY_BUFFER, BUFF_SIZE, vertices, GL_STATIC_DRAW);

    GLuint v_position_loc = glGetAttribLocation(program, "v_position");
    glEnableVertexAttribArray(v_position_loc);
    glVertexAttribPointer(v_position_loc, 4, GL_FLOAT, GL_FALSE, 0, 0);
}

void Skybox::loadCubemap() {
    glGenTextures(1, &vto);
    glBindTexture(GL_TEXTURE_CUBE_MAP, vto);

    int width, height, nrComponents;
    for (unsigned int i = 0; i < SKYBOX_FACES_COUNT; i++)
    {
        unsigned char* data = stbi_load(faces_paths[i].c_str(), &width, &height, &nrComponents, 0);
        if (data)
        {
            glTexImage2D(GL_TEXTURE_CUBE_MAP_POSITIVE_X + i, 0, GL_RGB, width, height, 0, GL_RGB, GL_UNSIGNED_BYTE, data);
            stbi_image_free(data);
        }
        else
        {
            cout << "Cubemap texture failed to load at path: " << faces_paths[i] << endl;
            stbi_image_free(data);
        }
    }
    glTexParameteri(GL_TEXTURE_CUBE_MAP, GL_TEXTURE_MIN_FILTER, GL_LINEAR);
    glTexParameteri(GL_TEXTURE_CUBE_MAP, GL_TEXTURE_MAG_FILTER, GL_LINEAR);
    glTexParameteri(GL_TEXTURE_CUBE_MAP, GL_TEXTURE_WRAP_S, GL_CLAMP_TO_EDGE);
    glTexParameteri(GL_TEXTURE_CUBE_MAP, GL_TEXTURE_WRAP_T, GL_CLAMP_TO_EDGE);
    glTexParameteri(GL_TEXTURE_CUBE_MAP, GL_TEXTURE_WRAP_R, GL_CLAMP_TO_EDGE);
}